package dio.trilha.java.basico;

public class operador2 {

	public static void main(String[] args) {
		int numero = 5;
		
		System.out.println(-numero);
		System.out.println("");
		
		System.out.println(numero);
		System.out.println("");
		
		numero = -numero;
		System.out.println(-numero);
		
		numero = numero * -1;
		System.out.println(numero);
	}

}
